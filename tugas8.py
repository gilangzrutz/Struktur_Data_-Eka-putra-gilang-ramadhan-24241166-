# Definisikan variabel number sebagai string agar bisa diakses per karakter
number = "1234567890"

# Menampilkan data awal 
print("Data Awal:", number[0])

# Menampilkan data akhir 
print("Data Akhir:", number[-1])

# Menampilkan data ke-3 sampai ke-6 
print("Data ke-3 sampai ke-6:", number[2:6])